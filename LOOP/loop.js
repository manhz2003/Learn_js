// for in, in ra chỉ mục của các phần tử trong mảng
let arr = ['manh', 'dung', 'nam', 'tuan']
for (let test in arr) {
    console.log(test)
}

// for of, in ra giá trị phần tử của mảng
for (let test of arr) {
    console.log(test)
}